/**
 * Addition
 */
export const add = (a: number, b: number) => {return a + b};

/**
 * Subtraction
 */
export const subtract = (a: number, b: number) => {return a - b};

/**
 * Multiply
 */
export const multiply = (a: number, b: number) => {return a * b};

/**
 * Divide
 */
export const divide = (a: number, b: number) => {return a / b};